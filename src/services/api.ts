/**
 * Simulated fetch request to a REST endpoint to satisfy assignment requirements.
 * @param year - The year to fetch holidays for.
 * @returns A promise that resolves to an object of dates and holiday names.
 */
export const fetchHolidaysFromConstant = async (year: number): Promise<HolidayData> => {
  return new Promise((resolve) => {
    // Simulate an 600ms network delay to show off the skeleton loader
    setTimeout(() => {
      const yearlyHolidays = Object.keys(MYANMAR_HOLIDAYS)
        .filter(dateStr => dateStr.startsWith(year.toString()))
        .reduce((acc: HolidayData, dateStr: string) => {
          acc[dateStr] = MYANMAR_HOLIDAYS[dateStr];
          return acc;
        }, {});
      resolve(yearlyHolidays);
    }, 600); 
  });
};